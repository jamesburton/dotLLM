// IQ2-family CUDA kernels for dotLLM.
//
// Implements 2-bit importance-quant decode for the three IQ2 layouts shipped by
// llama.cpp / GGUF:
//
//   IQ2_XXS (66 B / 256 elem, 2.0625 bpw): half d; uint16 qs[QK_K/8];
//   IQ2_XS  (74 B / 256 elem, 2.3125 bpw): half d; uint16 qs[QK_K/8]; uint8 scales[QK_K/32];
//   IQ2_S   (82 B / 256 elem, 2.5625 bpw): half d; uint8 qs[QK_K/4]; uint8 qh[QK_K/32]; uint8 scales[QK_K/32];
//
// IQ2_S is the on-disk format used by both the MOSTLY_IQ2_S and MOSTLY_IQ2_M
// file-type recipes (the popular ~11.5 GB Qwen3.6-A3B-IQ2_M.gguf is IQ2_S
// blocks for the 2-bit tensors).
//
// Each format encodes 8 dequantized values per "pair" via:
//   1. A codebook lookup (iq2{xxs,xs,s}_grid_device) -> 8 bytes of grid coords
//   2. Per-element +1/-1 from ksigns_iq2xs_device[sign_idx]
//   3. A per-pair scale = block-scale * (0.5 + sub-scale) * 0.25
//
// Layout, scale, and sign rules are bit-for-bit equivalent to
// dequantize_row_iq2_{xxs,xs,s} in ggml-quants.c.

