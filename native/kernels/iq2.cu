// =========================================================================
// IQ2 quantized GEMV decode kernels
// =========================================================================
// y[n] = W[n,k] @ x[k] with FP16 input/output and FP32 accumulation.
// One thread-block per output row; threads cooperatively walk the super-blocks
// and accumulate per-element products. Warp reduction collapses the partials.
//
// Each super-block contributes 256 elements. Per pair (8 elements) the kernel
// does: dl * sum_j (grid[gridIdx*8 + j] * sign_j * x[base+j]).

extern "C" __global__ void __launch_bounds__(256, 2) quantized_gemv_iq2_xxs(
    const uint8_t* __restrict__ weight,
    const half* __restrict__ x,
    half* __restrict__ y,
    const int n,
    const int k)
{
    int row = blockIdx.x;
    if (row >= n) return;

    const int superblocks_per_row = k / 256;
    const uint8_t* w_row = weight + (size_t)row * superblocks_per_row * IQ2_XXS_BLOCK_BYTES;
    float acc = 0.0f;

    for (int sb = threadIdx.x; sb < superblocks_per_row; sb += blockDim.x) {
        const uint8_t* block = w_row + sb * IQ2_XXS_BLOCK_BYTES;
        float d = __half2float(*reinterpret_cast<const half*>(block));
        const uint8_t* qs = block + 2;
        int base_x = sb * 256;

        for (int ib32 = 0; ib32 < 8; ib32++) {
            // Byte-wise load — IQ2_XXS qs is at block-offset 2 (uint16-aligned),
            // so the per-sub-block 8-byte slice is not guaranteed 4-byte-aligned.
            const uint8_t* p = qs + 8 * ib32;
            uint32_t a0 = (uint32_t)p[0] | ((uint32_t)p[1] << 8)
                        | ((uint32_t)p[2] << 16) | ((uint32_t)p[3] << 24);
            uint32_t a1 = (uint32_t)p[4] | ((uint32_t)p[5] << 8)
                        | ((uint32_t)p[6] << 16) | ((uint32_t)p[7] << 24);
            float db = d * (0.5f + (float)(a1 >> 28)) * 0.25f;
            for (int l = 0; l < 4; l++) {
                int grid_idx = (a0 >> (8 * l)) & 0xff;
                int signs_idx = (a1 >> (7 * l)) & 0x7f;
                uint8_t signs = ksigns_iq2xs_device[signs_idx];
                int grid_off = grid_idx * 8;
                int x_off = base_x + ib32 * 32 + l * 8;
                #pragma unroll 8
                for (int j = 0; j < 8; j++) {
                    float g = (float)iq2xxs_grid_device[grid_off + j];
                    float sign = (signs & (1 << j)) ? -1.0f : 1.0f;
                    acc += db * g * sign * __half2float(x[x_off + j]);
                }
            }
        }
    }

    // Warp reduction.
    for (int offset = warpSize / 2; offset > 0; offset >>= 1)
        acc += __shfl_down_sync(0xFFFFFFFF, acc, offset);

    __shared__ float warp_sums[32];
    int lane = threadIdx.x % warpSize;
    int warp_id = threadIdx.x / warpSize;
    if (lane == 0) warp_sums[warp_id] = acc;
    __syncthreads();

    if (warp_id == 0) {
        int num_warps = (blockDim.x + warpSize - 1) / warpSize;
        acc = (lane < num_warps) ? warp_sums[lane] : 0.0f;
        for (int offset = warpSize / 2; offset > 0; offset >>= 1)
            acc += __shfl_down_sync(0xFFFFFFFF, acc, offset);
    }
    if (threadIdx.x == 0) y[row] = __float2half(acc);
}

