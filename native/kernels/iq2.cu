// --------------------- IQ2_XXS dequant ---------------------
// 8 pairs of 8 elements per super-block; each pair reads (a0 byte index) +
// (a1 7-bit signs slot) from a packed uint32 stride-of-8 layout.
#define IQ2_XXS_BLOCK_SIZE 256
#define IQ2_XXS_BLOCK_BYTES 66

extern "C" __global__ void __launch_bounds__(256) dequant_iq2_xxs_f32(
    const uint8_t* __restrict__ src,
    float* __restrict__ dst,
    const int total_superblocks)
{
    int t = threadIdx.x; // 0..255 — one thread per element within a super-block

    for (int sb = blockIdx.x; sb < total_superblocks; sb += gridDim.x) {
        const uint8_t* block = src + (size_t)sb * IQ2_XXS_BLOCK_BYTES;
        float d = __half2float(*reinterpret_cast<const half*>(block));
        const uint8_t* qs = block + 2;

        int ib32 = t >> 5;       // 0..7
        int rem = t & 31;        // 0..31
        int l = rem >> 3;        // 0..3 (pair index within sub-block)
        int j = rem & 7;         // 0..7 (element index within pair)

        // IQ2_XXS blocks are 66 bytes — qs lands at block + 2, so the per-ib32
        // 8-byte slice can be 2-byte-aligned but not always 4-byte-aligned.
        // Read byte-wise into uint32s to side-step CUDA's strict alignment.
        const uint8_t* p = qs + 8 * ib32;
        uint32_t a0 = (uint32_t)p[0] | ((uint32_t)p[1] << 8)
                    | ((uint32_t)p[2] << 16) | ((uint32_t)p[3] << 24);
        uint32_t a1 = (uint32_t)p[4] | ((uint32_t)p[5] << 8)
                    | ((uint32_t)p[6] << 16) | ((uint32_t)p[7] << 24);
        int grid_idx = (a0 >> (8 * l)) & 0xff;
        int signs_idx = (a1 >> (7 * l)) & 0x7f;
        float db = d * (0.5f + (float)(a1 >> 28)) * 0.25f;
        uint8_t signs = ksigns_iq2xs_device[signs_idx];
        float g = (float)iq2xxs_grid_device[grid_idx * 8 + j];
        float sign = (signs & (1 << j)) ? -1.0f : 1.0f;
        dst[(size_t)sb * IQ2_XXS_BLOCK_SIZE + t] = db * g * sign;
    }
}

