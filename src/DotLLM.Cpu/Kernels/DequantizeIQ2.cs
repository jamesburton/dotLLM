        byte* blockBase = (byte*)src;
        int outIdx = 0;
        ReadOnlySpan<byte> grid = Iq2SGrid;

        for (long b = 0; b < blockCount; b++)
        {
            float d = (float)Unsafe.ReadUnaligned<Half>(blockBase);
            byte* qsLow = blockBase + 2;             // 32 bytes - low 8 bits of index per pair
            byte* qsSigns = blockBase + 2 + 32;      // 32 bytes - sign byte per pair (offset 34)
            byte* qh = blockBase + 2 + 64;           // 8 bytes - high 2 bits per pair (offset 66)
            byte* scales = blockBase + 2 + 64 + 8;   // 8 bytes - 4-bit pair scales (offset 74)

            for (int ib32 = 0; ib32 < KQuantGroupSize / 32; ib32++)
            {
                float db0 = d * (0.5f + (scales[ib32] & 0xF)) * 0.25f;
                float db1 = d * (0.5f + (scales[ib32] >> 4)) * 0.25f;
                for (int l = 0; l < 4; l++)
                {
                    // High 2 bits of grid index come from bits (2*l, 2*l+1) of qh[ib32].
                    int lo = qsLow[ib32 * 4 + l];
                    int hi = (qh[ib32] >> (2 * l)) & 0x3;
                    int gridIdx = lo | (hi << 8);
                    int signs = qsSigns[ib32 * 4 + l];
                    int gridOff = gridIdx * 8;
                    int outOff = outIdx + ib32 * 32 + l * 8;
                    float dl = l < 2 ? db0 : db1;
                    for (int j = 0; j < 8; j++)
                    {
                        float sign = (signs & (1 << j)) != 0 ? -1f : 1f;
                        dest[outOff + j] = dl * grid[gridOff + j] * sign;
                    }
                }
            }

            outIdx += KQuantGroupSize;
            blockBase += IQ2_S_BlockBytes;
        }
    }
