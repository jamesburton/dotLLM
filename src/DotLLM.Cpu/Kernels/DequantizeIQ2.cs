    /// <summary>
    /// Dequantizes IQ2_XXS. Block layout: <c>d(Half@0), qs[32](uint16)@2</c>.
    /// Each 32-element sub-block consumes 4 uint16 grid-index values and 1 uint16
    /// containing a 4-bit shared scale (top nibble of qs[3]) plus four 7-bit sign
    /// indices (low 28 bits split into 4x7).
    /// </summary>
    [SkipLocalsInit]
    internal static void DequantizeIQ2_XXS(nint src, long elementCount, Span<float> dest)
    {
        if (elementCount % KQuantGroupSize != 0)
            throw new ArgumentException(
                $"IQ2_XXS element count must be a multiple of {KQuantGroupSize}, got {elementCount}",
                nameof(elementCount));

        long blockCount = elementCount / KQuantGroupSize;
        byte* blockBase = (byte*)src;
        int outIdx = 0;
        ReadOnlySpan<byte> grid = Iq2XxsGrid;
        ReadOnlySpan<byte> ksigns = KsignsIq2Xs;

        for (long b = 0; b < blockCount; b++)
        {
            float d = (float)Unsafe.ReadUnaligned<Half>(blockBase);
            byte* qsBytes = blockBase + 2;

            // 8 sub-blocks of 32 elements each. Each sub-block reads 4 uint32s = 16 bytes from qs.
            // aux32[0] holds 4 little-endian uint8 grid indices (byte 0..3).
            // aux32[1] holds the 4-bit scale (top nibble) and four 7-bit sign indices.
            for (int ib32 = 0; ib32 < KQuantGroupSize / 32; ib32++)
            {
                uint a0 = Unsafe.ReadUnaligned<uint>(qsBytes + 8 * ib32);
                uint a1 = Unsafe.ReadUnaligned<uint>(qsBytes + 8 * ib32 + 4);
                float db = d * (0.5f + (a1 >> 28)) * 0.25f;
                for (int l = 0; l < 4; l++)
                {
                    int gridIdx = (int)((a0 >> (8 * l)) & 0xff);
                    int signsIdx = (int)((a1 >> (7 * l)) & 0x7f);
                    byte signs = ksigns[signsIdx];
                    int gridOff = gridIdx * 8;
                    int outOff = outIdx + ib32 * 32 + l * 8;
                    for (int j = 0; j < 8; j++)
                    {
                        float sign = (signs & (1 << j)) != 0 ? -1f : 1f;
                        dest[outOff + j] = db * grid[gridOff + j] * sign;
                    }
                }
            }

            outIdx += KQuantGroupSize;
            blockBase += IQ2_XXS_BlockBytes;
        }
    }

    /// <summary>
    /// Dequantizes IQ2_XS. Block layout:
    /// <c>d(Half@0), qs[32](uint16)@2, scales[8]@66</c>. Each uint16 packs a
    /// 9-bit codebook index (bits 0-8) plus a 7-bit sign index (bits 9-15).
    /// Each <c>scales</c> byte stores two 4-bit sub-scales (low/high nibble),
    /// applied to a pair of consecutive 8-element groups.
    /// </summary>
    [SkipLocalsInit]
    internal static void DequantizeIQ2_XS(nint src, long elementCount, Span<float> dest)
    {
        if (elementCount % KQuantGroupSize != 0)
            throw new ArgumentException(
                $"IQ2_XS element count must be a multiple of {KQuantGroupSize}, got {elementCount}",
                nameof(elementCount));

        long blockCount = elementCount / KQuantGroupSize;
        byte* blockBase = (byte*)src;
        int outIdx = 0;
        ReadOnlySpan<byte> grid = Iq2XsGrid;
        ReadOnlySpan<byte> ksigns = KsignsIq2Xs;

        for (long b = 0; b < blockCount; b++)
        {
            float d = (float)Unsafe.ReadUnaligned<Half>(blockBase);
            byte* qsBytes = blockBase + 2;
            byte* scales = blockBase + 2 + 64;

            for (int ib32 = 0; ib32 < KQuantGroupSize / 32; ib32++)
            {
                float db0 = d * (0.5f + (scales[ib32] & 0xF)) * 0.25f;
                float db1 = d * (0.5f + (scales[ib32] >> 4)) * 0.25f;
                for (int l = 0; l < 4; l++)
                {
                    ushort q = Unsafe.ReadUnaligned<ushort>(qsBytes + (ib32 * 4 + l) * 2);
                    int gridIdx = q & 0x1FF;
                    int signsIdx = q >> 9;
                    byte signs = ksigns[signsIdx];
                    int gridOff = gridIdx * 8;
                    int outOff = outIdx + ib32 * 32 + l * 8;
                    float dl = l < 2 ? db0 : db1;
                    for (int j = 0; j < 8; j++)
                    {
                        float sign = (signs & (1 << j)) != 0 ? -1f : 1f;
                        dest[outOff + j] = dl * grid[gridOff + j] * sign;
                    }
                }
            }

            outIdx += KQuantGroupSize;
            blockBase += IQ2_XS_BlockBytes;
        }
    }

    /// <summary>
