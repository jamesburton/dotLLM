    /// <summary>
    /// Dequantizes IQ1_S. Block layout:
    /// <c>d(Half@0), qs[32]@2, qh[8](uint16)@34</c>.
    /// Per 256-element super-block: 8 sub-blocks of 32 elements. Per sub-block ib in [0,8):
    /// <c>dl = d * (2*((qh[ib] >> 12) &amp; 7) + 1)</c>,
    /// <c>delta = (qh[ib] &amp; 0x8000) ? -IQ1S_DELTA : +IQ1S_DELTA</c>. Each sub-block
    /// has 4 groups of 8 elements; group <c>l</c> reads codebook entry
    /// <c>qs[ib*4 + l] | (((qh[ib] >> 3*l) &amp; 7) &lt;&lt; 8)</c> from the 2048-entry
    /// signed-int8 grid, then emits <c>dl * (grid[j] + delta)</c> per element.
    /// </summary>
    [SkipLocalsInit]
    internal static void DequantizeIQ1_S(nint src, long elementCount, Span<float> dest)
    {
        if (elementCount % KQuantGroupSize != 0)
            throw new ArgumentException(
                $"IQ1_S element count must be a multiple of {KQuantGroupSize}, got {elementCount}",
                nameof(elementCount));

        long blockCount = elementCount / KQuantGroupSize;
        byte* blockBase = (byte*)src;
        int outIdx = 0;
        ReadOnlySpan<ulong> grid = Iq1SGrid;

        for (long b = 0; b < blockCount; b++)
        {
            float d = (float)Unsafe.ReadUnaligned<Half>(blockBase);
            byte* qs = blockBase + 2;
            ushort* qh = (ushort*)(blockBase + 2 + 32);

            for (int ib = 0; ib < KQuantGroupSize / 32; ib++)
            {
                ushort qhVal = qh[ib];
                float dl = d * (2 * ((qhVal >> 12) & 7) + 1);
                float delta = (qhVal & 0x8000) != 0 ? -Iq1SDelta : Iq1SDelta;
                int qsBase = ib * 4;
                for (int l = 0; l < 4; l++)
                {
                    int idx = qs[qsBase + l] | (((qhVal >> (3 * l)) & 7) << 8);
                    ulong gridEntry = grid[idx];
                    int outOff = outIdx + ib * 32 + l * 8;
                    for (int j = 0; j < 8; j++)
                    {
                        sbyte g = (sbyte)((gridEntry >> (8 * j)) & 0xff);
                        dest[outOff + j] = dl * (g + delta);
                    }
                }
            }

            outIdx += KQuantGroupSize;
            blockBase += IQ1_S_BlockBytes;
        }
    }
